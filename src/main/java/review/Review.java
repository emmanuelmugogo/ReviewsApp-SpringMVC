package review;

public class Review {
	
	long id;
	String title;
	String imageUrl;
	String reviewCategory;
	String content;
	
	public long getId() {
		return id;
	}
	
	public String getTitle() {
		return title;
	}
	
	public String getImageUrl() {
		return imageUrl;
	}
	
	public String getReviewCategory() {
		return reviewCategory;
	}
	
	public String getContent() {
		return content;
	}
	
	

}
